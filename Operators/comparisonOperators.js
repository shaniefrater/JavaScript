// We use Comparison Operators to compare the value of something with something else
// The result of a Comparison Operator will always be a boolean (true or false)

let x = 1;

console.log(x > 0); // Returns true because you are comparing x to 0
console.log(x >= 1); // If x is greater than or equal to 1
console.log(x < 0); // Less than 0
console.log(x <= 1); // If x is less than or equal to 1

// (>, >=, <, <=) Are all Relational Operators

// ===, !-- Are all Equality Operators

console.log(x === 0); // Is equal to
console.log(x !== 0); // Is not equal to

