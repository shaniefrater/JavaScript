// There are two types of Equality Operators (===, ==)

console.log(1 === 1); // Strict Equality Operator - ensures both values either side of the operator have the same value. E.g. a string and string
console.log("1" === 1); // Strict Equality Operator - will return false automatically as both are different value types

console.log(1 == 1); // Loose Equality Operator
console.log('1' == 1); // Loose Equality Operator- still returns true as both operants are of the same value

// Strict quality operators ensure that both operants are of the same VALUE and TYPE
// Loose equality operators doesn't care about matching types, if the types do not match, it will convert what's on the right side to what's on the left


